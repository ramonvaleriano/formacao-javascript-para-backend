/*Incrementação de forma resumida.*/

let number = 1;
console.log(`Temos a variável number com o valor: ${number}`);
console.log(`Vamos somar 1 usando o ++`);

number++;
console.log(`Temos a variável number com o valor: ${number}`);

number += 5;
console.log(`Vamos soamr 5 usando o += 5: ${number}`);

number--;
console.log(`Vamos subtrair 1 usando -- : ${number}`);

number -= 2;
console.log(`Vamos subtrair 2 usando -= 2: ${number}`);
