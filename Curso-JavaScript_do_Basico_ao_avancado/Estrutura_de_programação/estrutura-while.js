/*Estrutura de Repetição While*/

// Criando um contador de trás para frente.
console.log("Criando um contador de trás para frente.");

let x = 10;

while (x > 0) {
  console.log(x);
  x -= 1;
}

// Criando um contado em ordem crescente.
console.log("Criando um contado de ordem crescente.");

x = 1;
while (x <= 10) {
  console.log(x);
  x += 1;
}
