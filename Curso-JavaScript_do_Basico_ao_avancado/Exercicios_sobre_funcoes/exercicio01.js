/*
    Exercício 01:

    Uma funçaõ que imprima 'Hello Worlrd'
*/

// Usando função convencional
function mesagemConvencional(){
    console.log('Hello World!')
}

mesagemConvencional()

// Usando variável para uma função
const mensagemVariavel = function(){
    console.log('Hello World!')
}

mensagemVariavel()

// Usando Arrow Function
const mesagemArrowFuncion = () => {
    console.log('Hello World!')
}

mesagemArrowFuncion()